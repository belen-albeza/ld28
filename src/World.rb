class PlayScene < Scene
  def initialize(window)
    super window
    @images = {
      :grass => Gosu::Image.new(@window, 'gfx/tiles/grass.png', true),
      :hero => Gosu::Image.new(@window, 'gfx/chara.png')
    }
    @map = Map.new(@images)
  end

  def button_up(key)
    @window.close if key == Gosu::KbEscape
  end

  def draw
    @images[:hero].draw(48, 48, 0)
    @map.draw
  end
end

