  def draw
    self.rows.times do |row|
      self.cols.times do |col|
        img = self.tile(col, row)
        img.draw(col * SIZE, row * SIZE, 0) unless img.nil?
      end
    end
  end

  def tile(x, y)
    case @tiles[y][x]
    when 0 then nil
    when 1 then @images[:grass]
    end
  end

  def rows
    @tiles.size
  end

  def cols
    @tiles.first.size
  end
